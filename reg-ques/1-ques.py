# Python Program to Add Two Numbers
# Solution-1 (with predefined variables)

num1 = 5
num2 = 3
sum = num1 + num2
print("The sum of", num1, "and", num2, "is:", sum)

# Solution-2 (with user input)
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))
sum = num1 + num2
print("The sum of", num1, "and", num2, "is:", sum)
           